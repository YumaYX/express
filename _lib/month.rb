# 日本時間（JST）で現在の月を取得して、月ごとの絵文字を表示するプログラム

# 日本時間で現在の月を取得
japan_time = Time.now.getlocal("+09:00")  # JST（日本標準時）に設定
current_month = japan_time.month  # 現在の月を取得

def month_to_emoji(month)
  emojis = {
    1 => "🎍",  # 1月 - 正月の飾り
    2 => "❤️",  # 2月 - バレンタインデー
    3 => "🎎",  # 3月 - ひな祭り、桜
    4 => "🌸",  # 4月 - 春の花、花見
    5 => "🎏",  # 5月 - 端午の節句、こいのぼり
    6 => "☔️", # 6月 - 梅雨
    7 => "🎇",  # 7月 - 花火、夏祭り
    8 => "🍉",  # 8月 - スイカ、夏
    9 => "🍁",  # 9月 - 紅葉
    10 => "🎃", # 10月 - ハロウィン
    11 => "🍂", # 11月 - 秋の風物詩
    12 => "🎅", # 12月 - クリスマス
  }

  # 月が1から12の範囲内であることを確認
  if month.between?(1, 12)
    emojis[month]
  else
    "無効な月です。1から12の範囲で入力してください。"
  end
end

# 現在の月を表示
puts "# #{month_to_emoji(current_month)}"
